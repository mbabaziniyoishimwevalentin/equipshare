export const initiatePayment = async (req: Request, res: Response) => {
  try {
    const { orderId } = req.body;
    const userId = (req as any).user?.id;
    const order = await prisma.order.findUnique({
      where: { id: Number(orderId) },
      include: { items: { include: { equipment: { include: { owner: true } } } }, renter: true },
    });
    if (!order) return res.status(404).json({ error: 'Order not found' });

    let payment = await prisma.payment.findUnique({ where: { orderId: order.id } });
    if (payment && payment.status === 'PAID') {
      return res.status(400).json({ error: 'This order has already been paid.' });
    }
    if (!payment) {
      payment = await prisma.payment.create({
        data: { amount: order.totalAmount, status: 'PAID', orderId: order.id, userId: userId || order.renterId },
      });
    } else {
      payment = await prisma.payment.update({ where: { id: payment.id }, data: { status: 'PAID' } });
    }

    await prisma.order.update({ where: { id: order.id }, data: { status: 'PROCESSING' } });

    const ownerIds = [...new Set(order.items.map((it: any) => it.equipment.ownerId))];
    for (const ownerId of ownerIds) {
      await prisma.notification.create({
        data: {
          userId: Number(ownerId),
          type: 'payment',
          title: 'New paid order',
          body: `Order ${order.orderNumber} was paid. Invoice available for download.`,
          read: false,
          linkUrl: `/owner/orders/${order.id}`,
        },
      });
    }

    sendInvoiceEmail(order).catch((err) => console.error('Failed to send invoice email:', err));

    return res.json({ success: true, message: 'Payment successful', paymentId: payment.id, orderId: order.id });
  } catch (error) {
    console.error('initiatePayment error', error);
    return res.status(500).json({ error: 'Failed to process payment' });
  }
};

// Webhook endpoint for PayPack payment events
export const paymentWebhook = async (req: Request, res: Response) => {
  try {
    const payload = req.body;
    console.log('[paymentWebhook] Received webhook:', JSON.stringify(payload, null, 2));

    // Extract payment/order information from various possible payload formats
    const status = payload?.data?.status || payload?.status || payload?.event;
    const meta = payload?.data?.meta || payload?.meta || {};
    const paymentId = meta.paymentId || payload?.data?.paymentId || payload?.paymentId;
    const orderId = meta.orderId || payload?.data?.orderId || payload?.orderId || payload?.order_id;

    console.log('[paymentWebhook] Status:', status, 'PaymentId:', paymentId, 'OrderId:', orderId);

    let payment = null;
    if (paymentId) {
      payment = await prisma.payment.findUnique({ where: { id: Number(paymentId) } });
    }
    if (!payment && orderId) {
      payment = await prisma.payment.findUnique({ where: { orderId: Number(orderId) } });
    }

    if (!payment) {
      console.warn('[paymentWebhook] Payment record not found for webhook');
      return res.json({ received: true });
    }

    // Normalize status and check if payment was successful
    const normalizedStatus = String(status).toLowerCase();
    const paidStatuses = ['successful', 'paid', 'completed', 'payment.completed', 'checkout.completed'];
    
    if (paidStatuses.includes(normalizedStatus)) {
      console.log('[paymentWebhook] Payment successful, updating payment and order status');
      
      await prisma.payment.update({ where: { id: payment.id }, data: { status: 'PAID' } });
      await prisma.order.update({ where: { id: payment.orderId }, data: { status: 'PROCESSING' } });

      const order = await prisma.order.findUnique({
        where: { id: payment.orderId },
        include: { items: { include: { equipment: true } }, renter: true },
      });
      
      if (order) {
        // Notify all equipment owners
        const owners = new Set<number>();
        for (const it of order.items) owners.add(it.equipment.ownerId);
        
        for (const ownerId of owners) {
          await prisma.notification.create({
            data: {
              userId: Number(ownerId),
              type: 'payment',
              title: 'New paid order',
              body: `Order ${order.orderNumber} was paid. Invoice available for download.`,
              read: false,
              linkUrl: `/owner/orders/${order.id}`,
            },
          });
        }

        // Send invoice email to renter (non-blocking)
        sendInvoiceEmail(order).catch((err) => console.error('[paymentWebhook] Failed to send invoice email:', err));
      }
    } else {
      console.log('[paymentWebhook] Payment status not successful, marking as PENDING');
      await prisma.payment.update({ where: { id: payment.id }, data: { status: 'PENDING' } });
    }

    return res.json({ received: true });
  } catch (error) {
    console.error('[paymentWebhook] ERROR:', error);
    return res.status(500).send('error');
  }
};

// ── Verify payment after redirect from PayPack ──────────────────────
export const verifyPayment = async (req: Request, res: Response) => {
  try {
    const orderId = Number(req.query.orderId || req.query.order_id || 0);
    const userId = (req as any).user?.id;

    console.log('[verifyPayment] Verifying payment for orderId:', orderId, 'userId:', userId);

    if (!orderId) {
      return res.status(400).json({ error: 'orderId is required' });
    }

    const order = await prisma.order.findUnique({
      where: { id: orderId },
      include: { renter: true, payment: true },
    });
    
    if (!order) {
      return res.status(404).json({ error: 'Order not found' });
    }
    
    if (order.renterId !== userId) {
      return res.status(403).json({ error: 'Forbidden' });
    }
    
    if (!order.payment) {
      return res.status(400).json({ error: 'No payment record found for this order' });
    }

    console.log('[verifyPayment] Payment status:', order.payment.status);
    return res.json({ success: true, paymentStatus: order.payment.status, orderId: order.id });
  } catch (error) {
    console.error('[verifyPayment] ERROR:', error);
    return res.status(500).json({ error: 'Verification failed' });
  }
};

// ── Download invoice PDF ───────────────────────────────────────────
export const getInvoice = async (req: Request, res: Response) => {
  try {
    const { orderId } = req.params;
    const userId = (req as any).user?.id;

    const order = await prisma.order.findUnique({
      where: { id: Number(orderId) },
      include: { items: { include: { equipment: true } }, renter: true },
    });
    if (!order) return res.status(404).json({ error: 'Order not found' });

    const isOwner = order.items.some((it: any) => it.equipment.ownerId === userId);
    if (order.renterId !== userId && !isOwner) {
      return res.status(403).json({ error: 'Forbidden' });
    }

    const pdfBuffer = await generateInvoicePdf(order);
    res.setHeader('Content-Type', 'application/pdf');
    res.setHeader('Content-Disposition', `attachment; filename=invoice-order-${order.id}.pdf`);
    res.end(pdfBuffer);
  } catch (error) {
    console.error('getInvoice error', error);
    res.status(500).json({ error: 'Failed to generate invoice' });
  }
};
