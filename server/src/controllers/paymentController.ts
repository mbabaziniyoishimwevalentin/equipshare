// ── Helper: generate invoice PDF buffer ──────────────────────────
async function generateInvoicePdf(order: any): Promise<Buffer> {
  let PDFDocument: any;
  try { PDFDocument = require('pdfkit'); } catch { throw new Error('pdfkit not installed'); }

  return new Promise((resolve, reject) => {
    const doc = new PDFDocument({ size: 'A4', margin: 50 });
    const buffers: Buffer[] = [];
    doc.on('data', (chunk: Buffer) => buffers.push(chunk));
    doc.on('end', () => resolve(Buffer.concat(buffers)));
    doc.on('error', reject);

    const pageWidth = doc.page.width - 100;
    const lm = 50;
    const rm = doc.page.width - 50;

    // ── Header Bar ──
    doc.rect(0, 0, doc.page.width, 85).fill('#0B215E');
    doc.fill('#ffffff').fontSize(22).font('Helvetica-Bold').text('EQUIPSHARE', lm, 22);
    doc.fontSize(9).font('Helvetica').text('Smart Rental Marketplace', lm, 50);
    doc.fill('#ffffff').fontSize(8).font('Helvetica');
    doc.text('INVOICE', rm - 60, 22, { width: 60, align: 'right' });
    doc.text(`#${order.orderNumber}`, rm - 60, 34, { width: 60, align: 'right' });

    // ── Invoice Meta ──
    const now = new Date();
    const dateStr = now.toLocaleDateString('en-GB', { day: '2-digit', month: 'short', year: 'numeric' });
    const timeStr = now.toLocaleTimeString('en-GB', { hour: '2-digit', minute: '2-digit' });

    let y = 105;
    doc.fill('#0B215E').fontSize(10).font('Helvetica-Bold').text('Invoice Details', lm, y);
    y += 16;
    doc.fontSize(8.5).font('Helvetica');
    const leftMeta = [
      `Order Date: ${new Date(order.createdAt).toLocaleDateString('en-GB', { day: '2-digit', month: 'short', year: 'numeric' })}`,
      `Invoice Date: ${dateStr}`,
      `Invoice Time: ${timeStr}`,
    ];
    const rn = order.renter;
    const renterName = rn ? `${rn.firstName || ''} ${rn.lastName || ''}`.trim() : '—';
    const ownerNames = [...new Set(order.items?.map((it: any) => {
      const o = it.equipment?.owner;
      return o ? `${o.firstName || ''} ${o.lastName || ''}`.trim() : null;
    }).filter(Boolean))];
    const rightMeta = [
      `Prepared by: EquipShare System`,
      `Generated by: ${ownerNames.length ? ownerNames.join(' & ') : renterName}`,
      `Status: Paid`,
    ];
    for (let i = 0; i < leftMeta.length; i++) {
      doc.fill('#555555').text(leftMeta[i], lm, y);
      doc.text(rightMeta[i], rm - 160, y, { width: 160, align: 'right' });
      y += 14;
    }

    // ── Bill To ──
    y += 8;
    doc.fill('#0B215E').fontSize(10).font('Helvetica-Bold').text('Bill To', lm, y);
    y += 15;
    doc.fill('#333333').fontSize(8.5).font('Helvetica')
      .text(renterName, lm, y)
      .text(`Email: ${rn?.email || '—'}`, lm, y + 12)
      .text(`Phone: ${rn?.phone || '—'}`, lm, y + 24);

    // ── Items Table Header ──
    y += 48;
    doc.rect(lm, y, rm - lm, 18).fill('#0B215E');
    doc.fill('#ffffff').fontSize(8).font('Helvetica-Bold');
    doc.text('Item', lm + 8, y + 4, { width: 170 });
    doc.text('Duration', lm + 178, y + 4, { width: 80 });
    doc.text('Rate', lm + 258, y + 4, { width: 70, align: 'right' });
    doc.text('Amount', lm + 328, y + 4, { width: 70, align: 'right' });

    // ── Items Table Rows ──
    y += 18;
    let ri = 0;
    for (const it of order.items) {
      const bg = ri % 2 === 0 ? '#f9f9f9' : '#ffffff';
      doc.rect(lm, y, rm - lm, 20).fill(bg);
      doc.fill('#333333').fontSize(7.5).font('Helvetica');
      doc.text((it.equipment?.title || '').substring(0, 30), lm + 8, y + 5, { width: 170 });
      doc.text(it.timeline || '—', lm + 178, y + 5, { width: 80 });
      doc.text(`Rwf ${Number(it.price ?? 0).toLocaleString()}`, lm + 258, y + 5, { width: 70, align: 'right' });
      doc.text(`Rwf ${Number(it.totalAmount ?? 0).toLocaleString()}`, lm + 328, y + 5, { width: 70, align: 'right' });
      y += 20;
      ri++;
    }

    // ── Totals ──
    y += 8;
    const itemsTotal = order.items?.reduce((s: number, it: any) => s + Number(it.totalAmount), 0) || 0;
    const displaySubtotal = order.subtotal || itemsTotal || order.totalAmount;
    const displayTax = order.tax || (order.subtotal ? 0 : itemsTotal * 0.18);
    const displayTotal = order.tax ? order.totalAmount : itemsTotal + itemsTotal * 0.18;
    const tx = rm - 180;
    const tw = 130;

    doc.fill('#555555').fontSize(8.5).font('Helvetica');
    doc.text('Subtotal', tx, y, { width: tw, align: 'right' });
    doc.text(`Rwf ${Number(displaySubtotal).toLocaleString()}`, tx + tw, y, { width: 50, align: 'right' });
    y += 16;

    doc.text('Tax (18%)', tx, y, { width: tw, align: 'right' });
    doc.text(`Rwf ${Number(displayTax).toLocaleString()}`, tx + tw, y, { width: 50, align: 'right' });
    y += 16;

    doc.moveTo(tx, y).lineTo(rm, y).stroke('#cccccc');
    y += 10;

    doc.fill('#0B215E').fontSize(12).font('Helvetica-Bold');
    doc.text('Total Paid (incl. tax)', tx, y, { width: tw, align: 'right' });
    doc.text(`Rwf ${Number(displayTotal).toLocaleString()}`, tx + tw, y, { width: 50, align: 'right' });

    // ── Footer ──
    y = doc.page.height - 65;
    doc.rect(0, y, doc.page.width, 65).fill('#0B215E');
    doc.fill('#ffffff').fontSize(7.5).font('Helvetica');
    doc.text('EquipShare — Smart Rental Marketplace | Thank you for your business!', lm, y + 14, { width: rm - lm, align: 'center' });
    doc.text(`Invoice #${order.orderNumber} | Generated on ${dateStr} at ${timeStr}`, lm, y + 28, { width: rm - lm, align: 'center' });
    doc.text('This is a computer-generated invoice.', lm, y + 42, { width: rm - lm, align: 'center' });

    doc.end();
  });
}

